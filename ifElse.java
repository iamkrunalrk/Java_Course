public class ifElse {
    public static void main( String[] args) {
        // If-else statements are used to make decisions in your code. 
        // They allow your program to execute different blocks of code depending on whether a given condition is true or false.

        int age = 18;

        if (age >= 18) {
            System.out.println("You are an adult.");
        } else {
            System.out.println("You are a minor.");
        }
        // In this example, the code checks whether age is greater than or equal to 18. 
        // If the condition is true, it prints "You are an adult." Otherwise, it prints "You are a minor."
    }
}
